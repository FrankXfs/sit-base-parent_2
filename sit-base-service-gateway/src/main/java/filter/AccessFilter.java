package filter;

import com.netflix.zuul.ZuulFilter;
import com.netflix.zuul.context.RequestContext;
import lombok.extern.slf4j.Slf4j;

import javax.servlet.http.HttpServletRequest;
@Slf4j
public class AccessFilter extends ZuulFilter {
    @Override
    public String filterType() {
        return "pre";
    }

    @Override
    public int filterOrder() {
        return 0;
    }

    @Override
    public boolean shouldFilter() {
        return true;
    }

    @Override
    public Object run() {
        RequestContext ctx = RequestContext.getCurrentContext();
        HttpServletRequest request = ctx.getRequest();
        log.info(String.format("%s request to %s , \n  ", request.getMethod(), request.getRequestURI())+"Authorization : "+request.getHeader("Authorization"));
        String path=request.getRequestURI();
        String Authorization = request.getHeader("Authorization");
        if(!path.equals("/token-service/session") && !path.equals("/feigntest/getsession") && (Authorization == null || Authorization.equals(""))) {
            log.warn("Authorization is empty");
            ctx.setSendZuulResponse(false);
            ctx.setResponseStatusCode(401);
            return null;
        }
        log.info("access token ok");
        return null;
    }
}
